package Day2;

import java.util.Scanner;

public class swichcase {
	
	
public void switchcase() {
	
	Scanner sc = new Scanner(System.in);
	int day = sc. nextInt();
	switch (day) {
	  case 1:
	    System.out.println("Meal");
	    break;
	  case 2:
	    System.out.println("Coffee");
	    break;
	  case 3:
	    System.out.println("Tea");
	    break;
	  
	}
}

}
